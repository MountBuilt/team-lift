// Escape a value for safe interpolation into HTML.
// Coerces non-strings via String(). No DOM/Firebase dependency — pure logic.
export function esc(str) {
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}
